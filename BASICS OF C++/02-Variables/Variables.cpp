// Variables

#include<iostream>     
using namespace std;   

int main() {
    // Declaring and initializing different types of variables

    int age = 20;                  // Integer variable to store whole numbers (e.g., 20)
    float weight = 55.4;           // Floating-point variable to store decimal numbers
    char grade = 'A';              // Character variable to store a single character
    string name = " Prathamesh Jadhav";  // String variable to store a sequence of characters (text)
    bool isStudent = false;        // Boolean variable to store true or false values

    // Displaying the values of variables using cout
    cout << "Age: " << age << endl;             // Outputs: Age: 20
    cout << "Weight: " << weight << endl;       // Outputs: Weight: 55.4
    cout << "Grade: " << grade << endl;         // Outputs: Grade: A
    cout << "Name: " << name << endl;           // Outputs: Name:  Prathamesh Jadhav
    cout << "Is Student: " << isStudent << endl; // Outputs: Is Student: 0 (false)

    return 0; // End of program
}
