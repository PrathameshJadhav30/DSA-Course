#include <iostream>
using namespace std;

int main() {
    int age = 20;  // Declare and initialize age

    // 'if' checks if the condition is true
    // If age is greater than or equal to 18, print eligible message
    if (age >= 18) {
        cout << "You are eligible to vote." << endl;
    }

    // If the condition is false, nothing will be printed
    return 0;
}
