#include <iostream>
using namespace std;

int main() {
    int marks = 45;  // Declare and initialize marks

    // If condition is true, "Passed" will be printed
    // If false, "Failed" will be printed
    if (marks >= 50) {
        cout << "Passed" << endl;
    } else {
        cout << "Failed" << endl;
    }

    return 0;
}
